public class TugasNestedLoop {
    public static void main(String args[]){
        int i=1;
        while(i<=5){//outer while loop
            int j=1;
            while(j<=i){//inner while loop
                System.out.print(j+" ");
                
                j++;
        }
        System.out.println();
        i++;
   }           
   }
}
